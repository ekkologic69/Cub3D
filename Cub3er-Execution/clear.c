#include "../cub3er.h"

void	clear_win(t_vars *vr)
{
	int	i;
	int	j;

	i = 0;
	while (i < 980)
	{
		j = 0;
		while (j < 1820)
		{
			my_mlx_pixel_put(&vr->data, j, i, 0X00000000);
			j++;
		}
		i++;
	}
}
